function cambiarIdioma(idioma) {
  localStorage.setItem("idioma", idioma); // Guardar idioma en localStorage
  aplicarTraduccion(idioma);
}

function aplicarTraduccion(idioma) {
  const elementos = document.querySelectorAll("[data-i18n]");
  elementos.forEach(el => {
    const key = el.getAttribute("data-i18n");
    const textoTraducido = traducciones[idioma][key];

    // Usa innerHTML si el texto incluye etiquetas HTML (como <br>)
    if (textoTraducido.includes("<br>") || textoTraducido.includes("<")) {
      el.innerHTML = textoTraducido;
    } else {
      el.textContent = textoTraducido;
    }
  });
}

// Al cargar la página
document.addEventListener("DOMContentLoaded", () => {
  const idiomaGuardado = localStorage.getItem("idioma") || "es";
  aplicarTraduccion(idiomaGuardado);
});
